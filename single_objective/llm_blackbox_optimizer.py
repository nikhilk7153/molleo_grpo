        # Sort by score and take top 10%
        zinc_scores.sort(key=lambda x: x[1], reverse=True)
        top_10_percent_size = max(n_samples, int(zinc_sample_size * 0.10))
        top_molecules = zinc_scores[:top_10_percent_size]
        
